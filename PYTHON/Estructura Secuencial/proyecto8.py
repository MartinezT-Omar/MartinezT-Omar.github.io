#Algoritmo que calcule la suma, resta, multiplicacion y division de dos numeros enteros

n1=float(input("Ingrese el primer numero entero: "))
n2=float(input("Ingrese el segundo numero entero: "))
suma=n1+n2
resta=n1-n2
multiplicacion=n1*n2
division=n1/n2
print("La suma de",n1,"+",n2,"=",suma)
print("La resta de",n1,"-",n2,"=",resta)
print("La multiplicacion de",n1,"*",n2,"=",multiplicacion)
print("La division de",n1,"/",n2,"=",division)
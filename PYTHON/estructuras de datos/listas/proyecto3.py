# lista

frutas=["apple", "banana", "cherry"]
#añadimos un elemento a la  lista
frutas.append("Orange")
#recorremos la lista
for x in frutas:
    print(x)
#definimos la longitud de la lista
longitud=len(frutas)
print("El tamaño o longitud es: ", longitud)


